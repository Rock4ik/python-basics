a = int(input("Введи первое число: "))
b = int(input("Введи второе число: "))
c = input("Введи символ '+' или '-' или '*' или '/': ")

if c == "+":
    print("Твой результат:" , a , "+", b, "=", a+b)
elif c == "-":
    print("Твой результат:" , a , "-", b, "=", a-b)
elif c == "*":
    print("Твой результат:" , a , "*", b, "=", a*b)
elif c == "/":
    print("Твой результат:" , a , "/", b, "=", a/b)
else:
    print("Ошибка!")